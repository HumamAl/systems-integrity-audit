// ─── Lookup Helpers ───────────────────────────────────────────────────────────

export const getSurfaceById = (id: string) =>
  integrationSurfaces.find((s) => s.id === id);

export const getFieldById = (id: string) =>
  canonicalFields.find((f) => f.id === id);

export const getFieldsBySource = (sourceId: string) =>
  canonicalFields.filter((f) => f.canonicalSourceId === sourceId);

export const getFieldsByConsumer = (consumerId: string) =>
  canonicalFields.filter((f) => f.consumerSurfaceIds.includes(consumerId));

export const getSyncEventsBySource = (sourceId: string) =>
  syncEvents.filter((e) => e.sourceSurfaceId === sourceId);

export const getSyncEventsByTarget = (targetId: string) =>
  syncEvents.filter((e) => e.targetSurfaceId === targetId);

export const getDriftIncidentsByField = (fieldId: string) =>
  driftIncidents.filter((d) => d.fieldId === fieldId);

export const getOpenDriftIncidents = () =>
  driftIncidents.filter(
    (d) =>
      d.resolutionStatus !== "resolved" &&
      d.resolutionStatus !== "accepted_divergence"
  );

export const getPathById = (id: string) =>
  integrationPaths.find((p) => p.id === id);

export const getWebhooksByStatus = (status: WebhookEndpoint["status"]) =>
  webhookEndpoints.filter((w) => w.status === status);

export const getJobsByStatus = (status: ScheduledJob["status"]) =>
  scheduledJobs.filter((j) => j.status === status);
