/** Returns an ISO datetime string N days in the past (negative = future) */
const daysAgo = (n: number, hh = "12", mm = "00", ss = "00"): string => {
  const d = new Date("2026-03-02T00:00:00Z");
  d.setDate(d.getDate() - n);
  return `${d.toISOString().split("T")[0]}T${hh}:${mm}:${ss}Z`;
};

const hoursAgo = (n: number): string => {
  const d = new Date("2026-03-02T14:00:00Z");
  d.setHours(d.getHours() - n);
  return d.toISOString();
};

